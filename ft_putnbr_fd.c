#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	num;

	num = 0;
	if (n < 0)
	{
		num = -(n % 10);
		n = -(n / 10);
		write(fd, "-", 1);
	}
	else
	{
		num = n % 10;
		n = n / 10;
	}
	if (n > 0)
	{
		ft_putnbr_fd(n, fd);
	}
	num += 48;
	write(fd, &num, 1);
}
